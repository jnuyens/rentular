// Build lookup maps for fast searching
const _byPostal = new Map<string, string[]>();
const _byCity = new Map<string, string[]>();

for (const [postal, city] of BELGIAN_POSTCODES) {
  if (!_byPostal.has(postal)) _byPostal.set(postal, []);
  _byPostal.get(postal)!.push(city);

  const lower = city.toLowerCase();
  if (!_byCity.has(lower)) _byCity.set(lower, []);
  _byCity.get(lower)!.push(postal);
}

export function citiesByPostalCode(postal: string): string[] {
  return _byPostal.get(postal) || [];
}

export function postalCodesByCity(cityName: string): string[] {
  const lower = cityName.toLowerCase();
  const results: string[] = [];
  for (const [city, postals] of _byCity.entries()) {
    if (city.includes(lower)) {
      results.push(...postals);
    }
  }
  return [...new Set(results)];
}

export function searchPostcodes(query: string): Array<{ postal: string; city: string }> {
  const lower = query.toLowerCase().trim();
  if (!lower) return [];

  const results: Array<{ postal: string; city: string }> = [];
  const isNumeric = /^\d+$/.test(lower);

  for (const [postal, city] of BELGIAN_POSTCODES) {
    if (isNumeric) {
      if (postal.startsWith(lower)) {
        results.push({ postal, city });
      }
    } else {
      if (city.toLowerCase().includes(lower)) {
        results.push({ postal, city });
      }
    }
    if (results.length >= 10) break;
  }
  return results;
}
